    /**
     * Conocer el numero de componentes que estan adicionados en el tab
     * @return numeroComponentes El numero de componentes
     */
    public int getNumerotabs() {
        numeroComponentes = jTabbedPanePrincipal.getTabCount();
        return numeroComponentes;          
    }

    /**
     * Configura las propiedades de los JTabbedPane que se adicionan a la gui.
     * @param tabPane El JTabbedPane que se va a configurar.
     */
    public void configurarJTabbedPane(String tituloTab, Component gui) {
        jTabbedPanePrincipal.add(tituloTab, gui ); // el titulo del TabComponent y el componente a mostrar
        jTabbedPanePrincipal.setTabLayoutPolicy(JTabbedPane.WRAP_TAB_LAYOUT);
    }

    public void initTabComponent(int indiceTab) {
        jTabbedPanePrincipal.setTabComponentAt(indiceTab, new ButtonTabComponent(jTabbedPanePrincipal));
    }
